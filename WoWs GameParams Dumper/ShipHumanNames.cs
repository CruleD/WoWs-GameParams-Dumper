        //https://developers.wargaming.net/reference/eu/wows/encyclopedia/ships/
        public static void ReadShipAPI()
        {
            SortedDictionary<string, string> TempShipList = new SortedDictionary<string, string>();
            if (ShipListNames == null)
            {
                ShipListNames = new Dictionary<string, string>();
            }
            else
            {
                ShipListNames.Clear();
            }

            bool MoreResults = false;
            using (WebClient WebClientTemp = new WebClient())
            {
                int PageIndex = 1;
                string JSONResponse = null;
                JObject JObjectTemp = null;
                string ShipName;
                string ShipStringID;
                do
                {
                    string AppID = null;
                    JSONResponse = WebClientTemp.DownloadString(string.Format("https://api.worldofwarships.eu/wows/encyclopedia/ships/?application_id={0}&page_no={1}", AppID, PageIndex));
                    JObjectTemp = JObject.Parse(JSONResponse);

                    foreach (JToken JTokenTemp in JObjectTemp["data"])
                    {
                        ShipName = JTokenTemp.First["name"].Value<string>();
                        if (ShipName.Substring(0, 1).Equals("[")) //Clan ships
                        {
                            continue;
                        }
                        ShipStringID = JTokenTemp.First["ship_id_str"].Value<string>();
                        if (!TempShipList.ContainsKey(ShipStringID))
                        {
                            TempShipList.Add(ShipStringID, ShipName);
                            ShipListNames.Add(ShipStringID, ShipName);
                        }
                    }

                    if (JObjectTemp["meta"]["count"].Value<int>() == 100)
                    {
                        PageIndex += 1;
                        MoreResults = true;
                    }
                    else
                    {
                        MoreResults = false;
                    }
                } while (MoreResults);
            }
            File.WriteAllText("ShipHumanNames.txt", JsonConvert.SerializeObject(TempShipList));

            foreach (KeyValuePair<string, string> KeyValuePairTemp in TempShipList)
            {
                Debug.WriteLine(string.Format("{{ \"{0}\", \"{1}\" }},", KeyValuePairTemp.Key, KeyValuePairTemp.Value));
            }
        }

